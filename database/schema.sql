CREATE DATABASE IF NOT EXISTS NutriSurvey;
USE NutriSurvey;

CREATE TABLE pengguna (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nama VARCHAR(100) NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    password VARCHAR(255) NOT NULL,
    tanggal_lahir DATE,
    jenis_kelamin VARCHAR(10),
    email_terverifikasi BOOLEAN DEFAULT FALSE,
    token_verifikasi VARCHAR(255),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

CREATE TABLE profil_nutrisi (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user_id INT NOT NULL,
    tinggi_badan DECIMAL(5,1),
    berat_badan_awal DECIMAL(5,1),
    berat_badan_target DECIMAL(5,1),
    usia INT,
    jenis_kelamin VARCHAR(10),
    target_kalori INT DEFAULT 2000,
    target_protein INT DEFAULT 60,
    target_lemak INT DEFAULT 50,
    target_karbohidrat INT DEFAULT 65,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES pengguna(id) ON DELETE CASCADE
);

CREATE TABLE menu_makanan (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nama_makanan VARCHAR(150) NOT NULL,
    kalori INT NOT NULL,
    protein DECIMAL(5,1) DEFAULT 0,
    lemak DECIMAL(5,1) DEFAULT 0,
    karbohidrat DECIMAL(5,1) DEFAULT 0,
    kategori VARCHAR(50),
    tipe_diet VARCHAR(50),
    gambar VARCHAR(255),
    emoji VARCHAR(10),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE konsumsi_harian (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user_id INT NOT NULL,
    menu_id INT,
    nama_makanan VARCHAR(150),
    porsi DECIMAL(5,1) DEFAULT 1,
    kalori INT DEFAULT 0,
    protein DECIMAL(5,1) DEFAULT 0,
    lemak DECIMAL(5,1) DEFAULT 0,
    karbohidrat DECIMAL(5,1) DEFAULT 0,
    tanggal DATE NOT NULL,
    waktu_makan VARCHAR(20) DEFAULT 'Sarapan',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES pengguna(id) ON DELETE CASCADE,
    FOREIGN KEY (menu_id) REFERENCES menu_makanan(id) ON DELETE SET NULL
);

CREATE TABLE berat_badan (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user_id INT NOT NULL,
    berat_badan DECIMAL(5,1) NOT NULL,
    tanggal DATE NOT NULL,
    catatan TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES pengguna(id) ON DELETE CASCADE
);

CREATE TABLE preferensi_pengguna (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user_id INT NOT NULL,
    target_kalori INT DEFAULT 2000,
    preferensi_makan VARCHAR(50) DEFAULT 'semua',
    alasan_diet VARCHAR(50) DEFAULT 'jaga',
    tingkat_aktivitas VARCHAR(20) DEFAULT 'sedang',
    alergi VARCHAR(255),
    jumlah_makan INT DEFAULT 3,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES pengguna(id) ON DELETE CASCADE
);

CREATE TABLE pengaturan_notifikasi (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user_id INT NOT NULL,
    pengingat_makan BOOLEAN DEFAULT TRUE,
    pengingat_minum BOOLEAN DEFAULT TRUE,
    pengingat_timbang BOOLEAN DEFAULT TRUE,
    waktu_pengingat TIME DEFAULT '08:00:00',
    sarapan_aktif BOOLEAN DEFAULT TRUE,
    sarapan_waktu TIME DEFAULT '07:00:00',
    siang_aktif BOOLEAN DEFAULT TRUE,
    siang_waktu TIME DEFAULT '12:00:00',
    malam_aktif BOOLEAN DEFAULT TRUE,
    malam_waktu TIME DEFAULT '18:00:00',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES pengguna(id) ON DELETE CASCADE
);

