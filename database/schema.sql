CREATE TABLE laporan_analisis (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user_id INT NOT NULL,
    periode ENUM('harian', 'mingguan', 'bulanan') NOT NULL,
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    total_kalori INT DEFAULT 0,
    total_protein DECIMAL(10,1) DEFAULT 0,
    total_karbohidrat DECIMAL(10,1) DEFAULT 0,
    total_lemak DECIMAL(10,1) DEFAULT 0,
    rata_rata_kalori INT DEFAULT 0,
    rata_rata_berat DECIMAL(5,1) DEFAULT NULL,
    hari_tercapai INT DEFAULT 0,
    total_hari INT DEFAULT 0,
    data_lengkap JSON DEFAULT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES pengguna(id) ON DELETE CASCADE,
    UNIQUE KEY unique_user_periode (user_id, periode, end_date)
);

